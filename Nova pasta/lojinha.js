function compra(){
    var opcao = prompt("Escolha uma opção de 1 a 5")

    switch(Number(opcao)){
        case 1:
            alert("Pastel")
            break;

            case 2:
                alert("Coxinha")
                break;

                case 3:
                    alert("Coca-Cola")
                    break;
                    
                    case 4:
                        alert("Pamonha")
                        break;

                        case 5:
                            alert("Bala")
                            break;
    }
}